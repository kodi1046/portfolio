#!/usr/bin/env node
/* =============================================================================
   build-articles.mjs
   -----------------------------------------------------------------------------
   Regenerates shared/articles.json from the .md files in shared/articles/.

   Workflow:
     1. Drop a new  my-article.md  into  shared/articles/
        (optionally start it with frontmatter — see below)
     2. Run:  node build-articles.mjs
     3. The manifest is updated; your new article appears on all three sites.

   Existing entries are PRESERVED (your hand-written titles/summaries/tags stay),
   so it is safe to run anytime. New files get sensible defaults pulled from the
   file: the first "# Heading" becomes the title, the first paragraph becomes the
   summary, and the date defaults to today — edit shared/articles.json to refine.

   Optional frontmatter at the top of a .md file overrides the defaults:
     ---
     title: My Article
     date: 2026-06-01
     summary: One-line description.
     tags: [Quant, Probability]
     ---
   ============================================================================= */
import { readFile, writeFile, readdir } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const here = path.dirname(fileURLToPath(import.meta.url));
const DIR = path.join(here, "shared", "articles");
const MANIFEST = path.join(here, "shared", "articles.json");

const slugify = (f) => f.replace(/\.md$/i, "").toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");
const today = () => new Date().toISOString().slice(0, 10);

function parseFrontmatter(md) {
  const m = md.match(/^---\s*\n([\s\S]*?)\n---\s*\n?/);
  if (!m) return { fm: {}, body: md };
  const fm = {};
  for (const line of m[1].split("\n")) {
    const i = line.indexOf(":");
    if (i === -1) continue;
    const k = line.slice(0, i).trim();
    let v = line.slice(i + 1).trim();
    if (v.startsWith("[") && v.endsWith("]")) v = v.slice(1, -1).split(",").map((s) => s.trim()).filter(Boolean);
    fm[k] = v;
  }
  return { fm, body: md.slice(m[0].length) };
}

function defaults(body, file) {
  const h = body.match(/^#\s+(.+)$/m);
  const title = h ? h[1].trim() : slugify(file).replace(/-/g, " ");
  const para = body.replace(/^#.*$/m, "").split(/\n\s*\n/).map((s) => s.trim()).find((s) => s && !s.startsWith("#") && !s.startsWith("*"));
  const summary = para ? para.replace(/[*_`]/g, "").slice(0, 160) : "";
  return { title, summary };
}

const existing = JSON.parse(await readFile(MANIFEST, "utf8").catch(() => '{"articles":[]}'));
const bySlug = new Map((existing.articles || []).map((a) => [a.slug, a]));

const files = (await readdir(DIR)).filter((f) => f.toLowerCase().endsWith(".md"));
const out = [];
for (const file of files) {
  const slug = slugify(file);
  if (bySlug.has(slug)) { out.push({ ...bySlug.get(slug), file }); continue; }
  const md = await readFile(path.join(DIR, file), "utf8");
  const { fm, body } = parseFrontmatter(md);
  const d = defaults(body, file);
  out.push({
    slug,
    title: fm.title || d.title,
    date: fm.date || today(),
    summary: fm.summary || d.summary,
    tags: Array.isArray(fm.tags) ? fm.tags : (fm.tags ? [fm.tags] : []),
    file
  });
  console.log("  + added", file);
}

out.sort((a, b) => (a.date < b.date ? 1 : -1));
const json = {
  _comment: "Auto-generated by build-articles.mjs. Safe to edit by hand; existing entries are preserved on rebuild.",
  articles: out
};
await writeFile(MANIFEST, JSON.stringify(json, null, 2) + "\n");
console.log(`articles.json updated — ${out.length} article(s).`);
